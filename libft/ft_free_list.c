#include "../philo.h"

int	ft_free_list(t_env *env)
{
	int	i;

	i = 0;
	if (env)
	{
		while (i < env->n_philo)
		{
			free (env->p);
			env->p = env->p->next;
			i++;
		}
	}
	return (0);
}
